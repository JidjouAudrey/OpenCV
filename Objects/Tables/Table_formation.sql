CREATE TABLE Formation(id_formation INT,
                       school VARCHAR (20),
                       fieldOfstudy VARCHAR(20),
                        diploma VARCHAR(20),
                       startDate DATE (15),
                       endDate DATE(15),
                       obtainedResult VARCHAR(20),
                       descriptionForm VARCHAR(25),
                       id_exp INT,
                       id_user INT,
                       id_cv INT
);